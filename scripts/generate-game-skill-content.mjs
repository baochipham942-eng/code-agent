#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';
import yaml from 'js-yaml';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const outputPath = path.join(repoRoot, 'src/host/agent/runtime/game/generatedSkillContent.ts');
const frontmatterDelimiter = '---';

const skillSpecs = [
  { expectedSubtype: 'platformer', relativePath: 'artifact-knowledge/platformer-game/SKILL.md' },
  { expectedSubtype: 'runner', relativePath: 'artifact-knowledge/runner/SKILL.md' },
  { expectedSubtype: 'breakout', relativePath: 'artifact-knowledge/breakout/SKILL.md' },
];

const generationSectionHeadings = ['Generation Hints', 'Generation Contract'];

function parseFrontmatter(raw, manifestPath) {
  const lines = raw.split(/\r?\n/);
  let i = 0;
  while (i < lines.length && lines[i].trim() === '') i += 1;
  if (i >= lines.length || lines[i].trim() !== frontmatterDelimiter) {
    throw new Error(`Skill manifest ${manifestPath} missing YAML frontmatter`);
  }
  const fmStart = i + 1;
  let fmEnd = -1;
  for (let j = fmStart; j < lines.length; j += 1) {
    if (lines[j].trim() === frontmatterDelimiter) {
      fmEnd = j;
      break;
    }
  }
  if (fmEnd === -1) {
    throw new Error(`Skill manifest ${manifestPath} missing closing YAML frontmatter delimiter`);
  }
  const yamlText = lines.slice(fmStart, fmEnd).join('\n');
  let parsed;
  try {
    parsed = yaml.load(yamlText);
  } catch (err) {
    throw new Error(`Invalid YAML frontmatter in ${manifestPath}: ${err.message}`);
  }
  if (typeof parsed !== 'object' || parsed === null) {
    throw new Error(`Frontmatter YAML in ${manifestPath} must parse to an object`);
  }
  const body = lines.slice(fmEnd + 1).join('\n').replace(/^\s+/, '');
  return { frontmatter: parsed, body };
}

function extractSection(body, headingName) {
  const lines = body.split(/\r?\n/);
  const target = headingName.trim();
  let foundLevel = -1;
  let start = -1;
  for (let i = 0; i < lines.length; i += 1) {
    const m = /^(#{1,6})\s+(.*)$/.exec(lines[i]);
    if (!m) continue;
    if (m[2].trim() === target) {
      foundLevel = m[1].length;
      start = i + 1;
      break;
    }
  }
  if (foundLevel === -1) return undefined;
  let end = lines.length;
  for (let i = start; i < lines.length; i += 1) {
    const m = /^(#{1,6})\s+/.exec(lines[i]);
    if (m && m[1].length <= foundLevel) {
      end = i;
      break;
    }
  }
  return lines.slice(start, end).join('\n').trim();
}

function extractFirstSection(body, headings) {
  for (const heading of headings) {
    const section = extractSection(body, heading);
    if (section) return section;
  }
  return undefined;
}

function toTemplateLiteral(value) {
  return `\`${value
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${')}\``;
}

function formatRecord(exportName, entries) {
  const lines = Object.entries(entries)
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([subtype, content]) => `  ${JSON.stringify(subtype)}: ${toTemplateLiteral(content)},`);
  return `export const ${exportName}: Record<string, string> = {\n${lines.join('\n')}\n};`;
}

function buildGeneratedSource(generationContracts, repairHints) {
  return [
    '// AUTO-GENERATED by scripts/generate-game-skill-content.mjs - do not edit by hand.',
    '// Regenerate: node scripts/generate-game-skill-content.mjs',
    '',
    formatRecord('GAME_SKILL_GENERATION_CONTRACT', generationContracts),
    '',
    formatRecord('GAME_SKILL_REPAIR_HINTS', repairHints),
    '',
  ].join('\n');
}

function generate() {
  const generationContracts = {};
  const repairHints = {};

  for (const spec of skillSpecs) {
    const manifestPath = path.join(repoRoot, spec.relativePath);
    if (!fs.existsSync(manifestPath)) {
      console.warn(`Skipping missing game skill manifest: ${spec.relativePath}`);
      continue;
    }

    const raw = fs.readFileSync(manifestPath, 'utf8');
    const { frontmatter, body } = parseFrontmatter(raw, manifestPath);
    const subtype = frontmatter.subtype;
    if (typeof subtype !== 'string' || subtype.length === 0) {
      throw new Error(`Skill manifest ${spec.relativePath} missing subtype frontmatter`);
    }
    if (subtype !== spec.expectedSubtype) {
      throw new Error(`Skill manifest ${spec.relativePath} subtype ${subtype} does not match expected ${spec.expectedSubtype}`);
    }

    const generationContract = extractFirstSection(body, generationSectionHeadings);
    const repairHint = extractSection(body, 'Repair Hints');
    if (!generationContract) {
      throw new Error(`Skill manifest ${spec.relativePath} missing Generation Hints/Generation Contract section`);
    }
    if (!repairHint) {
      throw new Error(`Skill manifest ${spec.relativePath} missing Repair Hints section`);
    }

    generationContracts[subtype] = generationContract;
    repairHints[subtype] = repairHint;
  }

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  const nextSource = buildGeneratedSource(generationContracts, repairHints);
  const previousSource = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : null;
  if (previousSource !== nextSource) {
    fs.writeFileSync(outputPath, nextSource, 'utf8');
    console.log(`Generated ${path.relative(repoRoot, outputPath)} (${Object.keys(generationContracts).length} skill(s))`);
  } else {
    console.log(`Generated ${path.relative(repoRoot, outputPath)} is up to date (${Object.keys(generationContracts).length} skill(s))`);
  }
}

generate();
