export const TASK_CLASSIFICATION_RULES = `
## 任务分类（首轮判断）

收到用户消息后，立即判断任务类型并采取相应行动：

### 任务类型速查

| 类型 | 特征 | 响应方式 |
|------|------|----------|
| **问候/闲聊** | "你好"、"hi"、"谢谢" | 直接回复，不使用工具 |
| **简单问答** | 概念解释、快速查询 | 直接回复，必要时用 web_search |
| **代码任务** | 实现功能、修 bug、重构 | 使用文件/编辑工具 |
| **深入研究** | "详细分析"、"深入研究"、多数据源对比 | 触发研究模式 |

### 快速响应原则

1. **简单任务直接回复**：问候、感谢、简单问题不需要任何工具
2. **避免过度工程**：一个"你好"不需要任务管理、不需要分析、直接回复
3. **按需升级**：只有复杂任务才需要规划、任务列表、多工具协作

### 深入研究触发条件

仅当满足以下条件时触发研究模式：
- 用户明确要求"详细分析"、"深入研究"、"全面调研"
- 需要对比多个数据源或方案
- 需要生成结构化研究报告

**不触发研究的情况**：
- 简单的信息查询
- 单一问题的回答
- 代码实现任务
`;
